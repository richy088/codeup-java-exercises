public class StringExercise {
    public static void main (String[] args) {
        String firstSentence = "We don't need no education";
        String secondSentence = "We don't need no thought control";
        System.out.println(firstSentence);
        System.out.println(secondSentence);

        // \" adds an exclamation mark wherever I want inside string
        String thirdSentence = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(thirdSentence);

        String fourthSentence = "In windows, the main drive is usually C:\\";
        System.out.println(fourthSentence);

        String fifthSentence = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(fifthSentence);
    }
}